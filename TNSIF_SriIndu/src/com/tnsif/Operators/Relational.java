package com.tnsif.Operators;

public class Relational {
    public static void main(String[] args) {
        int a = 25, b = 30;
        System.out.println("a == b: " + (a == b));
        System.out.println("a != b: " + (a != b));
        System.out.println("a > b: " + (a > b));
        System.out.println("a < b: " + (a < b));
    }
}